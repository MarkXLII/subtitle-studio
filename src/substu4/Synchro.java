    private String getLine()
    {
        String temp = "";
        while(iter < dataLength)
        {
            char ch = data.charAt(iter);
            if(ch != '\n')
                temp = temp + ch;
            else
            {
                iter++;
                if(temp.equals("") || temp.equals("\n"))
                    return getLine();
                return temp;
            }
            iter++;
        }
        return "\0";
    }
    @Action
    public void playPause() throws IOException 
    {
        if(!isStarted && !loading)
        {
            sync = true;
            isStarted = true;
            isPlaying = true;
            jButton1.setText("PAUSE");
            
            d0 = "...";
            d1 = getLine();
            d2 = getLine();
            d3 = getLine();
            d4 = getLine();
            setDialogs();
            if(d1.length() >=10)
                jButton2.setText("START "+'"'+d1.substring(0, 10)+"...");
            else
                jButton2.setText("START "+'"'+d1.substring(0, d1.length())+'"');
            writer = new BufferedWriter(new FileWriter(output));
            fileOpen = true;
            sw1 = new StopWatch();
            StopWatch.instance = 0;
            mediaPlayer.play();
            while(mediaPlayer.getTime() <=0);
            sw1.start();
            while(fileLength == 0)
                fileLength = mediaPlayer.getLength();
            jSlider1StateChanged(null);
        }       
        else if(isStarted)
        {
            if(isPlaying)
            {
                isPlaying = false;
                mediaPlayer.pause();
                sw1.pauseSW();
                jButton1.setText("PLAY");
            }
            else
            {
                isPlaying = true;
                mediaPlayer.play();
                sw1.resumeSW();
                jButton1.setText("PAUSE");
                jSlider1StateChanged(null);
            }
        }
    }
    
